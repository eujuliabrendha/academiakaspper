
public class TestaWhile {

	public static void main(String[] args) {

		int i = 0; // declarei uma variavel com nome i e lancei o valor 0

		while (i <= 10) {
			System.out.println(i); // escreveu o valor de i na tela
			i += 1; // incremento

			System.out.println("Saiu do laço");

		}

	}

}
